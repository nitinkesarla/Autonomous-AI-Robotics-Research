# Q3.2: Monte-Carlo Sampling to Estimate Values for Each State
def monte_carlo_sampling(env, policy, num_episodes, gamma, max_steps=100):
    """
    Use Monte-Carlo sampling to estimate the value of each state.
    Args:
        env: The environment (DungeonMazeEnv)
        policy: Policy {state: {action: prob}}
        num_episodes: Number of episodes to sample
        gamma: Discount factor
        max_steps: Maximum number of steps per episode
    Returns:
        Value estimates {state: value}, and visit counts {state: count}
    """
    # Initialize value estimates and visit counts for each state
    V = {s: 0.0 for s in states}
    visits = {s: 0 for s in states}

    for episode in range(num_episodes):
        # Sample a trajectory
        trajectory = rollout(env, policy, max_steps)

        # Calculate the return (cumulative reward) for each state in the trajectory
        # First-visit Monte-Carlo: Only count the first visit to each state
        visited_states = set()
        for t, (state, action, reward) in enumerate(trajectory):
            # Convert state to tuple if it's not already (in case of numpy types)
            state = (int(state[0]), int(state[1]), int(state[2]))

            # Skip if we've already visited this state in this episode
            if state in visited_states:
                continue
            visited_states.add(state)

            # Calculate the return starting from this step
            sub_trajectory = trajectory[t:]
            G = calculate_cumulative_reward(sub_trajectory, gamma)

            # Update the value estimate using incremental mean
            visits[state] += 1
            V[state] += (G - V[state]) / visits[state]

    return V, visits

