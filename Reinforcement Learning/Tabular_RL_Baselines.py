# Q2.2: Policy Improvement
def policy_improvement(V, policy, T, R, gamma):
    """
    Improve the policy by choosing the action that maximizes the expected value.
    Args:
        V: Value table {state: value}
        policy: Policy {state: {action: prob}}
        T: Transition model {state: {action: [(next_state, prob)]}}
        R: Reward model {state: {action: reward}}
        gamma: Discount factor
    Returns:
        Updated policy, and a boolean indicating if the policy changed
    """
    policy_stable = True
    new_policy = {}

    for s in states:
        if s == terminal_state:
            new_policy[s] = policy[s]  # Terminal state policy doesn't matter
            continue

        # Find the action that maximizes the expected value
        best_action = None
        best_value = float('-inf')
        for a in actions:
            # Compute the expected value for this action
            value = 0.0
            for next_s, prob in T[s][a]:
                value += prob * (R[s][a] + gamma * V[next_s])
            if value > best_value:
                best_value = value
                best_action = a

        # Update the policy to be greedy: choose the best action with probability 1
        new_policy[s] = {a: 1.0 if a == best_action else 0.0 for a in actions}

        # Check if the policy changed
        old_action = max(policy[s], key=policy[s].get)  # Action with highest prob in old policy
        if old_action != best_action:
            policy_stable = False

    return new_policy, policy_stable

