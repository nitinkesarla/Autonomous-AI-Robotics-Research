    # Track convergence
    episode = 0
    while episode < num_episodes:
        # Sample a trajectory
        trajectory = rollout(env, policy, max_steps)

        # Track the maximum change in values for this episode
        delta = 0.0

        # Perform TD updates for each step in the trajectory
        for t in range(len(trajectory)):
            state, action, reward = trajectory[t]
            state = (int(state[0]), int(state[1]), int(state[2]))

            # Get the next state (if not the last step)
            if t < len(trajectory) - 1:
                next_state = trajectory[t + 1][0]
                next_state = (int(next_state[0]), int(next_state[1]), int(next_state[2]))
            else:
                # If this is the last step, assume termination (next state is terminal)
                next_state = terminal_state

            # TD update
            td_target = reward + gamma * V[next_state]
            td_error = td_target - V[state]
            V[state] += alpha * td_error

            # Track the maximum change
            delta = max(delta, abs(alpha * td_error))

        episode += 1

        # Check for convergence
        if delta < theta:
            print(f"TD learning converged after {episode} episodes")
            break

    if episode == num_episodes:
        print(f"TD learning did not converge after {num_episodes} episodes")

    return V

