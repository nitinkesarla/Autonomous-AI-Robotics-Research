# Q2.1: Policy Evaluation
def policy_evaluation(V, policy, T, R, gamma, theta=1e-6, max_iterations=1000):
    """
    Evaluate the value function V for a given policy.
    Args:
        V: Value table {state: value}
        policy: Policy {state: {action: prob}}
        T: Transition model {state: {action: [(next_state, prob)]}}
        R: Reward model {state: {action: reward}}
        gamma: Discount factor
        theta: Convergence threshold
        max_iterations: Maximum number of iterations
    Returns:
        Updated value table V
    """
    for iteration in range(max_iterations):
        delta = 0.0
        # Update each state
        for s in states:  # Only update non-terminal states
            if s == terminal_state:
                continue
            v_old = V[s]
            # Compute new value using Bellman equation
            v_new = 0.0
            for a in actions:
                pi_a = policy[s][a]  # Probability of taking action a
                # Sum over possible next states (deterministic in this case)
                for next_s, prob in T[s][a]:
                    v_new += pi_a * prob * (R[s][a] + gamma * V[next_s])
            V[s] = v_new
            # Track the maximum change in value
            delta = max(delta, abs(v_old - V[s]))
        # Check for convergence
        if delta < theta:
            print(f"Policy evaluation converged after {iteration + 1} iterations")
            break
    else:
        print(f"Policy evaluation did not converge after {max_iterations} iterations")
    return V

