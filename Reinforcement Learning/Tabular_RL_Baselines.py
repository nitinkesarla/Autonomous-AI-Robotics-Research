# Q3.1: Cumulative Reward Calculation Function
def calculate_cumulative_reward(trajectory, gamma):
    """
    Calculate the cumulative discounted reward for a trajectory.
    Args:
        trajectory: List of tuples [(state, action, reward), ...]
        gamma: Discount factor
    Returns:
        Cumulative reward (float)
    """
    cumulative_reward = 0.0
    for t, (_, _, reward) in enumerate(trajectory):
        cumulative_reward += (gamma ** t) * reward
    return cumulative_reward

