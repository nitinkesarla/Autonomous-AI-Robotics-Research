# Step 3: Modify the DungeonMazeEnv to adjust rewards and allow an empty maze
class AdjustedDungeonMazeEnv(DungeonMazeEnv):
    def __init__(self, render_mode=None, grid_size=6, empty_maze=False):
        super().__init__(render_mode=render_mode, grid_size=grid_size)
        self.empty_maze = empty_maze

    def step(self, action):
        # Get the position in front of the agent (similar to DungeonMazeEnv.step)
        position_in_front = self.robot_position.copy()
        if self.robot_direction == Directions.north:
            position_in_front[1] -= 1
        elif self.robot_direction == Directions.east:
            position_in_front[0] += 1
        elif self.robot_direction == Directions.south:
            position_in_front[1] += 1
        elif self.robot_direction == Directions.west:
            position_in_front[0] -= 1

        # Check if the position in front is within bounds
        x, y = position_in_front
        if x < 0 or x >= self.grid_size or y < 0 or y >= self.grid_size:
            # Out of bounds, treat as a wall collision
            reward = -20  # Wall penalty (as defined in AdjustedDungeonMazeEnv)
            done = True
            truncated = False
            info = {}
            next_state = self.get_observations()  # Current state (agent doesn't move)
            return next_state, reward, done, truncated, info

        # If in bounds, proceed with the parent step method
        next_state, reward, done, truncated, info = super().step(action)

        # Adjust the step penalty
        if reward == -1:  # Original step penalty
            reward = -0.1  # New step penalty
        elif reward == -100:  # Original wall penalty
            reward = -20  # New wall penalty

        # Add a distance-based reward
        agent_pos = next_state["robot_position"]
        target_pos = next_state["target_position"]
        distance = np.linalg.norm(agent_pos - target_pos)
        max_distance = np.sqrt(2 * (self.grid_size - 1)**2)  # Max distance in grid (diagonal)
        distance_reward = (max_distance - distance) / max_distance  # Normalized distance reward (0 to 1)
        reward += distance_reward  # Small positive reward for getting closer to the target

        return next_state, reward, done, truncated, info

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        # Create the grid capturing the maze as walls
        self.maze = MazeGrid(size=self.grid_size, empty=self.empty_maze, np_rng=self.np_random)
        # Set the target location
        self.target_position = np.array([self.grid_size - 2, self.grid_size - 2])
        # Set the robot's location, direction, initial camera view
        self.robot_position = np.array([1, 1])
        self.robot_direction = Directions.south
        self.robot_camera_view = self.get_robot_camera_view()
        # Update the observations
        observation = self.get_observations()
        if self.render_mode == "human":
            self._render_frame()
        return observation, {}

    def get_robot_camera_view(self):
        # Get the position in front of the agent
        position_in_front = self.robot_position.copy()
        if self.robot_direction == Directions.north:
            position_in_front[1] -= 1
        elif self.robot_direction == Directions.east:
            position_in_front[0] += 1
        elif self.robot_direction == Directions.south:
            position_in_front[1] += 1
        elif self.robot_direction == Directions.west:
            position_in_front[0] -= 1

        # Check if the position is within bounds
        x, y = position_in_front
        if x < 0 or x >= self.grid_size or y < 0 or y >= self.grid_size:
            # Out of bounds, so assume it's a wall
            return np.ones((20, 20), dtype=np.int32) * 255

        # Get the contents of the cell in front of the agent
        cell_in_front = self.maze.get_cell_item(*position_in_front)

        if cell_in_front is None:
            # Shouldn't happen due to the bounds check, but keep for safety
            return np.ones((20, 20), dtype=np.int32) * 255
        elif cell_in_front == 1:
            # Wall
            return np.ones((20, 20), dtype=np.int32) * 255
        else:
            # Empty space
            return np.zeros((20, 20), dtype=np.int32)  # Fixed the syntax error

